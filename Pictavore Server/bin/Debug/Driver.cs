        {
            string cmd;
            int steps;

            //focuserPosition = Position; // Set the focuser position

            if (position > 0) cmd = "FOC+";
            else cmd = "FOC-";
            steps = Math.Abs(position);

            CommandString(steps + " FOCSTP !");
            CommandString(cmd);
            focuserPosition += position;
            tl.LogMessage("Move", position.ToString() + " " + steps.ToString() + cmd);

        }

